    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
         FileInputStream fis=null;
         
         if(file==null)
         {
             JOptionPane.showMessageDialog(rootPane, "Select image first");
         }
         else
         {
             try 
        {
           fis=new FileInputStream(file);
        }
        catch (Exception e) 
        {
            e.printStackTrace();
        }
       
        String item_id=jTextField1.getText();
        String item_name=jTextField2.getText();
        String price=jTextField3.getText();
        String category=(String)jComboBox1.getSelectedItem();
        String desc=jTextArea1.getText();
        String quantity=jTextField4.getText();
        
        try 
            (
                Connection con=dbconnection.dbConnection.getConnect();
                PreparedStatement ps=con.prepareStatement("insert into items values(?,?,?,?,?,?,?)")
            )
        {
            ps.setString(1,item_id);
            ps.setString(2,item_name);
            ps.setString(3,price);
            ps.setString(4,category);
            ps.setString(5,desc);
            ps.setString(6,quantity);
            ps.setBinaryStream(7, fis,file.length());
           int i=ps.executeUpdate();
           if(i>0)
           {
               JOptionPane.showMessageDialog(rootPane, "item added successfully");
               jTextField1.setText("");
               jTextField2.setText("");
               jTextField3.setText("");
               jTextField4.setText("");
               jTextArea1.setText("");
               jComboBox1.setSelectedIndex(0);
               jLabel9.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/select image.png")));
               generateId();
               
           }
           else
           {
               JOptionPane.showMessageDialog(rootPane, "error try again !!","ERROR",JOptionPane.ERROR_MESSAGE);
           }
        }
        catch (Exception e) 
        {
            e.printStackTrace();
        }
        
         }
    }//GEN-LAST:event_jButton2ActionPerformed

